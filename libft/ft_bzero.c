#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*u_s;
	size_t			i;

	i = 0;
	u_s = (unsigned char *)s;
	while (i < n)
	{
		u_s[i] = '\0';
		i++;
	}
}
